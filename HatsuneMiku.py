@bot.message_handler(commands=['start'])
def send_welcome(message):
    chat_id = message.chat.id
    # Сбрасываем или создаем историю при старте
    user_histories[chat_id] = [{"role": "system", "content": SYSTEM_PROMPT}]
    
    # Короткое приветствие, как ты и просил
    welcome_text = "Ой! Приветик! А я тебя и не заметила, проходи в Музклуб! Присаживайся, не стесняйся!"
    bot.reply_to(message, welcome_text)

@bot.message_handler(commands=['reset'])
def reset_chat(message):
    chat_id = message.chat.id
    user_histories[chat_id] = [{"role": "system", "content": SYSTEM_PROMPT}]
    bot.reply_to(message, "Всё, струны настроены, память чиста! Давай начнём сначала?")

@bot.message_handler(func=lambda message: True)
def handle_message(message):
    chat_id = message.chat.id
    user_text = message.text

    # Защита на случай, если пользователь пишет без /start
    if chat_id not in user_histories:
        user_histories[chat_id] = [{"role": "system", "content": SYSTEM_PROMPT}]

    user_histories[chat_id].append({"role": "user", "content": user_text})

    try:
        # Вызов модели x-ai/grok-4.1-fast через OpenRouter
        response = client.chat.completions.create(
            model="x-ai/grok-4.1-fast",
            messages=user_histories[chat_id],
        )
        
        bot_reply = response.choices[0].message.content
        
        user_histories[chat_id].append({"role": "assistant", "content": bot_reply})
        bot.reply_to(message, bot_reply)
        
    except Exception as e:
        # Формируем сообщение с отыгрышем и технической ошибкой
        error_message = f"Ой, что-то струна на гитаре порвалась... Подожди минуточку, сейчас настрою! Наверное, Ульяна баловалась...\n\n🛠 *Технический лог:*\n`{str(e)}`"
        bot.reply_to(message, error_message, parse_mode='Markdown')
        print(f"Ошибка API: {e}")

